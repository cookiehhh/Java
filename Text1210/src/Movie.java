
public class Movie {
	private int id;
	private String name;
	private	int p;
	public Movie(int id, String name, int p) {
		this.id = id;
		this.name = name;
		this.p = p;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getP() {
		return p;
	}
	public void setP(int p) {
		this.p = p;
	}
	
	
}

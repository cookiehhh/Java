
public class Student {
	String NO;
	String name;
	int age;
	int score;
	

public Student( String NO,String name,int age, int score) {
	this.NO=NO;
	this.name=name;
	this.age=age;
	this.score=score;
}
public String getNO() {
	return NO;
} 
public String getname() {
	return name;
}

public int getage() {
	return age;
}

public int getscore() {
	return score;
}


}
